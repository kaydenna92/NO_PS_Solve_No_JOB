# 규칙 찾기, 홀수면 만들 수가 없다!!!!!!!
# 1 => 0
# 2 => 3 = 3  
# 3 => 0
# 4 => dp[2] * dp[2] + 2 = 11
# 5 => 0
# 6 => dp[4] * dp[2] + dp[2] * 2 + 2 = 41
# 7 => 0
# 8 => dp[6] * dp[2] + dp[4] * 2 + dp[2] * 2 + 2 = 153
# 규칙 : dp[n] = dp[n - 2] * dp[2] + dp[n - 4] * 2 ... + 2


n = int(input())
dp = [0]*(31) # n+1로하면 런타임 에러..............
dp[2] = 3 

for i in range(4, n+1):
    if i % 2 == 0:
        dp[i] = dp[i-2] * dp[2] + sum(dp[:i-2]) * 2 + 2

    else: # 홀수면 만들 수 없음.
        dp[i] = 0

print(dp[n])